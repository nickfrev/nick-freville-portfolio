	rotate(x, y, z, inAng) {
		x = x * Math.cos(inAng.y) + z * Math.sin(inAng.y);
		z = z * Math.cos(inAng.y) - x * Math.sin(inAng.y);
		return [x * this.scale, -y * this.scale, z * this.scale];
	}

	draw() {
		let vert;
		let pos;
		let offX = this.canvas.width / 2;
		let offY = this.canvas.height / 2;

		let seen;
		let vertH = [];
		let a, b;
		const inObj = this.obj;
		const inAng = this.ang;

		for (let i = 0; i < this.sides.length; i++) {
			this.context.beginPath();
			seen = true;
			for (let j = 0; j < this.sides[i].length; j++) {
				vert = this.sides[i][j];

				pos = this.rotate(inObj.x[vert], inObj.y[vert], inObj.z[vert], inAng);
				this.context.lineTo(pos[0] + offX, pos[1] + offY);
				if (j < 3) {
					vertH[j] = [pos[0], pos[1], pos[2]];
				}
				if (j == 2) {
					a = [vertH[1][0] - vertH[0][0], vertH[1][1] - vertH[0][1], vertH[1][2] - vertH[0][2]];
					b = [vertH[2][0] - vertH[1][0], vertH[2][1] - vertH[1][1], vertH[2][2] - vertH[1][2]];
					if (a[0] * b[1] - a[1] * b[0] > 0) {
						seen = false;
						break;
					}
				}
			}
			this.context.closePath();
			if (seen) {
				this.context.strokeStyle = '#ff00ff';
				this.context.stroke();
			}
		}
	}

